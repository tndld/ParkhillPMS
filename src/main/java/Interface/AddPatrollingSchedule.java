    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        
        String typeOptionStr = (String) typeOption.getSelectedItem();
        
        String checkpoint = checkpointTF.getText();
        String timeStart = timeStartTF.getText();
        String timeEnd = timeEndTF.getText();
        String status = "To check-in";
        
        User.PatrollingSchedule ps = new User.PatrollingSchedule(checkpoint);
        
        if (checkpointTF.getText().isEmpty() || timeStartTF.getText().isEmpty() ||
               timeEndTF.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please insert all the required information!", "Error", JOptionPane.ERROR_MESSAGE);
        
        }else {

            if(ps.addSchedule(checkpoint, typeOptionStr, timeStart, timeEnd, status)){
                JOptionPane.showMessageDialog(this, "Schedule added successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
                checkpointTF.setText("");
                timeStartTF.setText("");
                timeEndTF.setText("");
                    
            }else {
                JOptionPane.showMessageDialog(this, "Failed to add new schedule.", "Error", JOptionPane.ERROR_MESSAGE);
                checkpointTF.setText("");
                timeStartTF.setText("");
                timeEndTF.setText("");
            } 
        }
        
    }//GEN-LAST:event_addBtnActionPerformed

